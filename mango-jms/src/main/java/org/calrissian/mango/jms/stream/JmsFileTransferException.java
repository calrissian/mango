package org.calrissian.mango.jms.stream;

public class JmsFileTransferException extends Exception {

    public JmsFileTransferException() {
    }

    public JmsFileTransferException(String s) {
        super(s);
    }

    public JmsFileTransferException(Throwable throwable) {
        super(throwable);
    }

    public JmsFileTransferException(String s, Throwable throwable) {
        super(s, throwable);
    }

}
